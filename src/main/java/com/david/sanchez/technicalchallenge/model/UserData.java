package com.david.sanchez.technicalchallenge.model;

public class UserData {
    private String strFirstName;
    private String strLastName;
    private String strMail;
    private String strLanguage;
    private String strCity;
    private String strZipCode;
    private String strPassword;
    private String strCheckPassword;
    private String strVerifyRegister;

    public String getStrFirstName() {
        return strFirstName;
    }

    public void setStrFirstName(String strFirstName) {
        this.strFirstName = strFirstName;
    }

    public String getStrLastName() {
        return strLastName;
    }

    public void setStrLastName(String strLastName) {
        this.strLastName = strLastName;
    }

    public String getStrMail() {
        return strMail;
    }

    public void setStrMail(String strMail) {
        this.strMail = strMail;
    }

    public String getStrLanguage() {
        return strLanguage;
    }

    public void setStrLanguage(String strLanguage) {
        this.strLanguage = strLanguage;
    }

    public String getStrCity() {
        return strCity;
    }

    public void setStrCity(String strCity) {
        this.strCity = strCity;
    }

    public String getStrZipCode() {
        return strZipCode;
    }

    public void setStrZipCode(String strZipCode) {
        this.strZipCode = strZipCode;
    }

    public String getStrPassword() {
        return strPassword;
    }

    public void setStrPassword(String strPassword) {

        this.strPassword = strPassword;
    }

    public String getStrCheckPassword() {

        return strCheckPassword;
    }

    public void setStrCheckPassword(String strCheckPassword) {

        this.strCheckPassword = strCheckPassword;
    }

    public String getStrVerifyRegister() {
        return strVerifyRegister;
    }

    public void setStrVerifyRegister(String strVerifyRegister) {
        this.strVerifyRegister = strVerifyRegister;
    }

}
